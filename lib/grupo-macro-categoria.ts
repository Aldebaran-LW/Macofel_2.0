/** Normaliza rótulo de grupo para chave de pesquisa (trim, espaços, maiúsculas, sem acentos). */
export function normalizeGrupoKey(raw: string | null | undefined): string {
  return String(raw ?? '')
    .normalize('NFD')
    .replace(/\p{M}/gu, '')
    .replace(/\u00a0/g, ' ')
    .trim()
    .replace(/\s+/g, ' ')
    .toUpperCase();
}

