/** Grupos que usam só a categoria escolhida no painel (fallback). */
const SKIP_NORMALIZED = new Set([
  '',
  'SEM GRUPO',
  'IMPORTADO PDF',
  'TESTE GRUPO',
]);

function buildMap(): Record<string, MacroCategorySlug> {
  const m: Record<string, MacroCategorySlug> = {};
  for (const [label, slug] of ENTRIES) {
    m[normalizeGrupoKey(label)] = slug;
  }
  return m;
}

export const GRUPO_MACRO_SLUG: Readonly<Record<string, MacroCategorySlug>> =
  buildMap();

/** Slug da categoria macro para o grupo; `null` → usar `categoryId` de fallback na importação. */
export function macroCategorySlugForGrupo(
  grupo: string | null | undefined
): MacroCategorySlug | null {
  const k = normalizeGrupoKey(grupo);
  if (SKIP_NORMALIZED.has(k)) return null;
  return GRUPO_MACRO_SLUG[k] ?? null;
}
